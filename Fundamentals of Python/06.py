# Number Data type

# Integers
num1 =  2 # discrete numbers 
print(type(num1))

# Floating point number
num2 = 2.0 # contains decimal value
print(type(num2))

# Complex numbers

compnum1 = 2+4j
compnum2 = complex(4,9)
print(compnum1.real,compnum1.imag)
print(compnum2.real,compnum2.imag)

print(type(compnum1))
